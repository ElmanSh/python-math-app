class calculator :
    def __init__(self, num1, num2) :
        self.num1=num1
        self.num2=num2
    def sum(self):
        return self.num1+self.num2
    def Submission(self):
        return self.num1-self.num2
    def Multiplication(self):
        return self.num1*self.num2
    def Division(self):
        return self.num1/self.num2
